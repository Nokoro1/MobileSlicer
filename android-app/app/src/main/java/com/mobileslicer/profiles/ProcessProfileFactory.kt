internal fun processProfileFieldDefaultSnapshot(): Map<String, Any?> =
    processProfileFieldDefaults.toMap()

internal fun processProfileValues(overrides: Iterable<Pair<Int, Any?>>): Map<String, Any?> {
    val values = processProfileFieldDefaults.toMap().toMutableMap()
    overrides.forEach { (index, value) ->
        val key = processProfileLegacyIndexNames[index] ?: processProfileFieldDefaults.getOrNull(index)?.first
        if (key != null) values[key] = value
    }
    return values
}

internal fun newProcessProfileUnchecked(vararg overrides: Pair<Int, Any?>): ProcessProfile =
    ProcessProfile(*overrides)
