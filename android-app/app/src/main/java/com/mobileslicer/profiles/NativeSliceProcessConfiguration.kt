internal fun String.asNativeConfigJsonObjectOrNull(): JSONObject? =
    takeIf { it.isNotBlank() }?.let { raw -> runCatching { JSONObject(raw) }.getOrNull() }

internal fun JSONObject?.nativeResolvedString(key: String): String {
    if (this == null || !has(key) || isNull(key)) return ""
    return when (val value = opt(key)) {
        is JSONArray -> {
            for (index in 0 until value.length()) {
                val scalar = value.opt(index)?.toString()?.trim()?.trim('"').orEmpty()
                if (scalar.isNotBlank() && scalar != "nil") return scalar
            }
            ""
        }
        null -> ""
        else -> value.toString().trim().trim('"').takeUnless { it == "nil" }.orEmpty()
    }
}

internal fun JSONObject?.nativeResolvedDouble(key: String): Double? =
    nativeResolvedString(key).toDoubleOrNull()

internal fun JSONObject?.nativeResolvedBoolean(key: String): Boolean? =
    when (nativeResolvedString(key).trim().lowercase(java.util.Locale.US)) {
        "1", "true", "yes", "on" -> true
        "0", "false", "no", "off" -> false
        else -> null
    }
