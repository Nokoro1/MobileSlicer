internal fun normalizedProcessSeamGap(value: String): String {
    val trimmed = value.trim()
    return when {
        trimmed.isBlank() -> DEFAULT_SEAM_GAP
        trimmed == "10%" -> DEFAULT_SEAM_GAP
        else -> trimmed
    }
}

